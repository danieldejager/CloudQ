    private String idEvidence;
    private String elaboration;
    private String idVendor;

    public String getIdVendorAnswers() {
        return idVendorAnswers;
    }

    public void setIdVendorAnswers(String idVendorAnswers) {
        this.idVendorAnswers = idVendorAnswers;
    }

